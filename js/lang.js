// ─── Language Engine ───────────────────────────────────────────────────────────

const LangSystem = (() => {
  const STORAGE_KEY = 'tirelomate_lang';
  let current = localStorage.getItem(STORAGE_KEY) || 'en';

  // Map of data-lang-key attributes → which element property to update
  function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;

    document.querySelectorAll('[data-lang-key]').forEach(el => {
      const key = el.getAttribute('data-lang-key');
      if (t[key] !== undefined) {
        if (el.tagName === 'INPUT') {
          el.placeholder = t[key];
        } else {
          el.textContent = t[key];
        }
      }
    });

    // Update html lang attribute
    document.documentElement.lang = lang === 'tn' ? 'tn' : 'en';
  }

  function toggle() {
    current = current === 'en' ? 'tn' : 'en';
    localStorage.setItem(STORAGE_KEY, current);
    applyTranslations(current);
  }

  function init() {
    applyTranslations(current);

    // Support both dedicated toggle button and repurposed chatbot bubble
    const btn = document.getElementById('langToggleBtn') || document.getElementById('chatbotToggle');
    if (btn) btn.addEventListener('click', toggle);
  }

  return { init, toggle, get: () => current, t: (key) => translations[current][key] || key };
})();

document.addEventListener('DOMContentLoaded', () => LangSystem.init());
